    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();
if (fila == -1) {
    JOptionPane.showMessageDialog(null, "FILA NO SELECCIONADA");
} else {
    id = Integer.parseInt(TablaDatos.getValueAt(fila, 0).toString());
    String nombre = TablaDatos.getValueAt(fila, 1).toString();
    String edad = TablaDatos.getValueAt(fila, 2).toString();
    String sexo = TablaDatos.getValueAt(fila, 3).toString();
    String peso = TablaDatos.getValueAt(fila, 4).toString();
    boolean hijo = Boolean.parseBoolean(TablaDatos.getValueAt(fila, 5).toString());
    int hijoscnt = Integer.parseInt(TablaDatos.getValueAt(fila, 6).toString());
    String nota = TablaDatos.getValueAt(fila, 7).toString();

    txtID.setText(String.valueOf(id));
    txtNOMBRE.setText(nombre);
    txtEDAD.setText(edad);
    txtSEXO.setText(sexo);
    txtPESO.setText(peso);
    chkHIJOS.setSelected(hijo);
    spinCANTIDAD.setValue(hijoscnt);

    // Convertir la nota de nuevo al valor del slider
    switch (nota) {
        case "A":
            slNOTA.setValue(0);
            break;
        case "B":
            slNOTA.setValue(5);
            break;
        case "C":
            slNOTA.setValue(10);
            break;
        default:
            slNOTA.setValue(0); // Valor por defecto si la nota no es A, B o C
    }
}


    }//GEN-LAST:event_TablaDatosMouseClicked


    public void Listar() {
        String sql = "SELECT*FROM practica;";

        try {
            cn = con.getConexion();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] usuario = new Object[8];
            modelo = (DefaultTableModel) TablaDatos.getModel();

            while (rs.next()) {
                usuario[0] = rs.getInt("id");
                usuario[1] = rs.getString("nombre");
                usuario[2] = rs.getInt("edad");
                usuario[3] = rs.getString("sexo");
                usuario[4] = rs.getString("peso");
                usuario[5] = rs.getString("hijos");
                usuario[6] = rs.getInt("hijoscant");
                usuario[7] = rs.getString("nota");

                modelo.addRow(usuario);
            }

            TablaDatos.setModel(modelo);

        } catch (Exception e) {
            System.out.println("ERROR EN EL METODO LISTAR" + e.toString());
        }
    }

    //M E T O D O || A G R E G A R
    public void Agregar() {
        String nombre = txtNOMBRE.getText();
        String edad = txtEDAD.getText();
        String sexo = txtSEXO.getText();
        String peso = txtPESO.getText();
        boolean hijos = chkHIJOS.isSelected();
        int cnthijos = (int) spinCANTIDAD.getValue();
        String nota;

        int sliderValue = slNOTA.getValue();
        if (sliderValue <= 3) {
            nota = "A";
        } else if (sliderValue <= 6) {
            nota = "B";
        } else {
            nota = "C";
        }

        if (nombre.equals("") || edad.equals("") || sexo.equals("") || peso.equals("") || Integer.toString(cnthijos).equals("") || nota.equals("")) {
            JOptionPane.showMessageDialog(null, "FALTAN VALORES");
        } else {
            {
                String sql = "INSERT INTO practica (nombre, edad, sexo, peso, hijos, hijoscant, nota) VALUES ('" + nombre + "', " + edad + ", '" + sexo + "', '" + peso + "', " + hijos + ", " + cnthijos + ", '" + nota + "');";

                try {
                    cn = con.getConexion();
                    st = cn.createStatement();
                    st.executeUpdate(sql);
                    System.out.println("SE AGREGO CORRECTAMENTE");
                    Limpiar();
                } catch (Exception e) {
                    System.out.println("NO SE LOGRO AGREGAR CORRECTAMENTE" + e.toString());
                }
            }
        }
    }

    public void Limpiar() {
        for (int i = 0; i < TablaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

